package pkg6.array;

/**
 *
 * @author priyanshusingh
 */
public class Array {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int[] ages = {20, 21, 22, 23};
        System.out.println("Age on Index 0 " + ages[0]);
        System.out.println("Age on Index 1 " + ages[1]);
        System.out.println("Age on Index 2 " + ages[2]);
        System.out.println("Age on Index 3 " + ages[3]);
        System.out.println("Size of Array is " + ages.length);
        
        String[] names = {"Priyanshu", "Williams", "Adams", "Nick"};
        System.out.println("Age on Index 0 " + names[0]);
        System.out.println("Age on Index 1 " + names[1]);
        System.out.println("Age on Index 2 " + names[2]);
        System.out.println("Age on Index 3 " + names[3]);
        System.out.println("Size of Array is " + names.length);
    }
    
}
